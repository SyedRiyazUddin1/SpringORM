package com.nisum.corejava.java8.demo;

public class Department {

	public Department() {

	}

	public Department(String name) {
		this.name = name;
	}

	private String name;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	@Override
	public String toString() {
		return name;
	}

}
