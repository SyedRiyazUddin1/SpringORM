package com.nisum.corejava.controlstructures.examples;

public class ExampleTernary {
	public static void main(String[] args) {

		int num = 15;

		//using ternary operator, if num is greater than 10, it will print the first part, else the second part
		
		System.out.println(num > 10 ? "num is greater than 10" : "num is lesser than 10");
	}
}
